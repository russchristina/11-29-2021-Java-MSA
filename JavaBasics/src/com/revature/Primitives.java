package com.revature;

/*
 * Java has 8 built-in primitive data types are used to represent the most basic data.
 */

public class Primitives {
	
	// Bitter Black Coffee Sure Is For Long Days
	boolean isTrueOrFalse = true;       // Boolean value of True or false
	byte aByte = 127;                   // Byte which only goes to 127
	char aCharacter = 'b';              // Character that can only hold a single character
	short aShort = 4;                   // A short integer
	int anInteger = 100;                // A normal integer
	float aFloat = 98.5f;               // A floating point or decimal number
	long aLong = 126546266923064565L;   // A long integer
	double aDouble = 54254.26554;       // A larger floating point or decimal number
	
}
